    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         int row = tableStudents.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // assuming model columns: 0=id,1=name,2=age,3=grade
    Object idObj = tableStudents.getValueAt(row, 0);
    if (idObj == null) {
        JOptionPane.showMessageDialog(this, "Selected row has no ID.", "Invalid Row", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int id;
    try {
        id = Integer.parseInt(idObj.toString());
    } catch (NumberFormatException nfe) {
        JOptionPane.showMessageDialog(this, "Selected ID is invalid.", "Invalid ID", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
            "Delete student with ID = " + id + " ?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) return;

    // now delete (same as above)
    Connection con = null;
    PreparedStatement pst = null;
    try {
        con = DBConnection.connect();
        String sql = "DELETE FROM students WHERE id = ?";
        pst = con.prepareStatement(sql);
        pst.setInt(1, id);
        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Deleted.");
            btnLoadActionPerformed(null); // refresh table
        } else {
            JOptionPane.showMessageDialog(this, "No record deleted (maybe already removed).");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    } finally {
        try { if (pst != null) pst.close(); } catch (Exception ex) {}
        try { if (con != null) con.close(); } catch (Exception ex) {}
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

